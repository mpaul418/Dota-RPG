package src;
import java.util.Random;

public class Character 
{	
	private Random r = new Random();
	protected String name;
	protected int HP, maxHP;
	protected int mana, maxMana;
	protected int level;
	protected int damage, maxDamage;
	protected int attack, maxAttack;
	protected int defense, maxDefense;
	protected int magicDefense, maxMagicDefense;
	protected int[] stats;
	protected boolean alive;

	//0- str; 1- dex; 2- int; 3-spd; 4-luck;??????
	
	public Character(int initHP, int initMana, int initDmg, int initAtk, int initDef, int initMagDef, String initName)
	{
		maxHP = initHP;
		HP = initHP;
		maxMana = initMana;
		mana = initMana;
		level = 1;
		maxDamage = initDmg;
		damage = initDmg;
		maxAttack = initAtk;
		attack = initAtk;
		maxDefense = initDef;	
		defense = initDef;
		maxMagicDefense = initMagDef;
		magicDefense = initMagDef;
		name = initName;
	}
	public int getHP()
	{
		return HP;
	}
	public int getMaxHP()
	{
		return maxHP;
	}
	public int getMana()
	{
		return mana;
	}
	public int getMaxMana()
	{
		return maxMana;
	}
	public int getLevel()
	{
		return level;
	}
	public int getDamage()
	{
		return damage;
	}
	public int getMaxDamage()
	{
		return maxDamage;
	}
	public int getAttack()
	{
		return attack;
	}
	public int getMaxAttack()
	{
		return maxAttack;
	}
	public int getDefense()
	{
		return defense;
	}
	public int getMaxDefense()
	{
		return maxDefense;
	}
	public int getMagicDefense()
	{
		return magicDefense;
	}
	public int getMaxMagicDefense()
	{
		return maxMagicDefense;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String s)
	{
		name = s;
	}
	public void changeMana(int amount)
	{
		mana += amount;
		if(mana > maxMana)
			mana = maxMana;
	}
	public void changeMaxMana(int amount)
	{
		maxMana += amount;
		mana += amount;
	}
	public void changeHP(int amount)
	{
		HP += amount;
		if(HP > maxHP)
			HP = maxHP;
	}
	public void changeMaxHP(int amount)
	{
		maxHP += amount;
		HP += amount;
	}
	public void changeDamage(int amount)
	{
		damage += amount;
		if(damage > maxDamage)
			damage = maxDamage;
	}
	public void changeMaxDamage(int amount)
	{
		maxDamage += amount;
		damage += amount;
	}
	public void changeDefense(int amount)
	{
		defense += amount;
		if(defense > maxDefense)
			defense = maxDefense;
	}
	public void changeMaxDefense(int amount)
	{
		maxDefense += amount;
		defense += amount;
	}
	public void changeMagicDefense(int amount)
	{
		magicDefense += amount;
		if(magicDefense > maxMagicDefense)
			magicDefense = maxMagicDefense;
	}
	public void changeMaxMagicDefense(int amount)
	{
		maxMagicDefense += amount;
		magicDefense += amount;
	}
	public void changeAttack(int amount)
	{
		attack += amount;
		if(attack > maxAttack)
			attack = maxAttack;
	}
	public void changeMaxAttack(int amount)
	{
		maxAttack += amount;
		attack += amount;
	}
	public boolean isAlive()
	{
		if(getHP() > 0)
			alive = true;
		else
			alive = false;
		return alive;
	}
	
	public void damage(Character c, int incoming_damage)
	{
		c.HP -= incoming_damage;
	}
	public void heal(Character c, int heal)
	{
		c.HP += heal;
	}
	public void attack(Character c)
	{
		int temp, damage_done;
		temp = 90 + r.nextInt(attack + 1);
		if(temp >= 100)
		{
			//FIXME damage dealt can be zero???? test
			//FIXME
			//FIXME
			int currentDefense = r.nextInt(c.getDefense() + 1);
			if(damage > currentDefense)
			{
				damage_done = damage - currentDefense;
				damage(c, damage_done);
				System.out.println(this.getName() + " attacked " + c.getName() + " and dealt " + damage_done + " damage!!"
							     + " (Defense roll: " + currentDefense + "/" + c.getMaxDefense() + ")\n");
			}
			else
			{
				damage_done = 1;
				damage(c, damage_done);
				System.out.println(this.getName() + " grazed " + c.getName() + " and dealt " + damage_done + " damage.\n"
								 + " (Defense roll: " + currentDefense + "/" + c.getMaxDefense() + ")\n");
			}
		}
		else
		{
			System.out.println(this.getName() + "'s attack missed! (Attack roll: " + temp + "\n");
		}
	}
	public void hunkerDown()
	{
		int amount = r.nextInt(31) + 30;
		this.defense += amount;
		System.out.println(this.getName() + " hunkered down, increasing its defense by " + amount + " for one turn!!\n");
	}
}